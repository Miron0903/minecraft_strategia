# стратегии/берсерк.py
"""
БЕРСЕРК - Агрессивная стратегия для реального сервера
Получает карту с сервера 188.120.254.248 и принимает решения
"""


def стратегия(карта):
    """
    карта - словарь с реальными данными с сервера
    """

    # Данные игрока
    я = карта['игрок']

    # Враги рядом
    враги = карта['враги']

    # Ресурсы рядом
    ресурсы = карта['ресурсы']

    # Если враги рядом - атакуем ближайшего
    if враги:
        # Находим ближайшего врага
        ближайший = min(враги, key=lambda в:
        abs(в['x'] - я['x']) + abs(в['z'] - я['z']))

        # Если враг очень близко - атакуем
        if abs(ближайший['x'] - я['x']) < 2 and abs(ближайший['z'] - я['z']) < 2:
            return {
                'действие': 'атаковать',
                'цель': ближайший
            }

        # Иначе идем к врагу
        return {
            'действие': 'двигаться',
            'dx': 1 if ближайший['x'] > я['x'] else -1,
            'dz': 1 if ближайший['z'] > я['z'] else -1
        }

    # Если нет врагов, ищем ресурсы
    if ресурсы:
        ближайший_ресурс = min(ресурсы, key=lambda р:
        abs(р['x'] - я['x']) + abs(р['z'] - я['z']))

        return {
            'действие': 'двигаться',
            'dx': 1 if ближайший_ресурс['x'] > я['x'] else -1,
            'dz': 1 if ближайший_ресурс['z'] > я['z'] else -1
        }

    # Если ничего нет - идем вперед
    return {
        'действие': 'двигаться',
        'dx': 1,
        'dz': 0
    }