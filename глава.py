# глава.py - Управляет битвой на реальном сервере
import time
import random
import importlib
from pathlib import Path
from бот_мост import MinecraftКарта
from карта_сервера import КартаСервера, ТипКлетки


class Глава:
    """👑 Управляет битвой стратегий на сервере 188.120.254.248"""

    def __init__(self):
        self.сервер_ip = "188.120.254.248"
        self.порт = 25565
        self.мост = MinecraftКарта()
        self.карта_сервера = КартаСервера(self.мост)
        self.стратегии = []
        self.боты = {}

    def загрузить_стратегии(self, папка="стратегии"):
        """Загружает все стратегии из папки"""
        print(f"\n📂 Загрузка стратегий из {папка}/...")

        путь = Path(папка)
        if not путь.exists():
            print(f"❌ Папка {папка} не найдена!")
            return

        for файл in путь.glob("*.py"):
            if файл.name == "__init__.py":
                continue

            try:
                # Импортируем модуль
                spec = importlib.util.spec_from_file_location(
                    файл.stem, файл
                )
                модуль = importlib.util.module_from_spec(spec)
                spec.loader.exec_module(модуль)

                # Ищем функцию стратегии
                if hasattr(модуль, 'стратегия'):
                    self.стратегии.append({
                        'имя': файл.stem,
                        'функция': модуль.стратегия,
                        'модуль': модуль
                    })
                    print(f"  ✅ {файл.name}")
                else:
                    print(f"  ⚠️ {файл.name} - нет функции 'стратегия'")

            except Exception as e:
                print(f"  ❌ {файл.name}: {e}")

        print(f"✅ Загружено стратегий: {len(self.стратегии)}")

    def создать_ботов(self):
        """Создает ботов для каждой стратегии"""
        print(f"\n🤖 Создание ботов на сервере {self.сервер_ip}...")

        for i, стратегия in enumerate(self.стратегии):
            имя_бота = f"MB_{i + 1}_{стратегия['имя'][:6]}"

            if self.мост.создать_бота(имя_бота):
                self.боты[имя_бота] = {
                    'имя': имя_бота,
                    'стратегия': стратегия['функция'],
                    'хп': 100,
                    'еда': 100,
                    'уровень': 1,
                    'убийств': 0
                }
                print(f"  ✅ {имя_бота} ({стратегия['имя']})")
                time.sleep(2)  # Пауза между подключениями

    def запустить_битву(self, раундов=100):
        """Запускает битву между стратегиями"""
        print("\n" + "=" * 60)
        print("⚔️  БИТВА НА СЕРВЕРЕ НАЧИНАЕТСЯ!")
        print(f"🌐 Сервер: {self.сервер_ip}:{self.порт}")
        print(f"🤖 Участников: {len(self.боты)}")
        print("=" * 60 + "\n")

        for раунд in range(1, раундов + 1):
            print(f"\n🔄 Раунд {раунд}")

            for имя_бота, данные in self.боты.items():
                # Получаем реальную карту с сервера
                карта = self.карта_сервера.получить_карту_для_стратегии(имя_бота)

                # Стратегия принимает решение
                решение = данные['стратегия'](карта)

                # Выполняем решение на сервере
                self.выполнить_решение(имя_бота, решение)

                # Проверяем здоровье
                if данные['хп'] <= 0:
                    print(f"💀 {имя_бота} погиб!")
                    self.мост.отправить_сообщение(имя_бота, "💀 Я ПОВЕРЖЕН!")

            # Каждые 10 раундов показываем статистику
            if раунд % 10 == 0:
                self.показать_статистику()

            time.sleep(3)  # Пауза между раундами

    def выполнить_решение(self, имя_бота, решение):
        """Выполняет решение стратегии на сервере"""
        if not решение:
            return

        действие = решение.get('действие')

        if действие == 'двигаться':
            dx = решение.get('dx', 0)
            dz = решение.get('dz', 0)

            if dx > 0:
                self.мост.двигать_бота(имя_бота, 'right', abs(dx))
            elif dx < 0:
                self.мост.двигать_бота(имя_бота, 'left', abs(dx))

            if dz > 0:
                self.мост.двигать_бота(имя_бота, 'back', abs(dz))
            elif dz < 0:
                self.мост.двигать_бота(имя_бота, 'forward', abs(dz))

        elif действие == 'атаковать':
            self.мост.отправить_сообщение(имя_бота, "⚔️ АТАКА!")
            # Здесь логика атаки

        elif действие == 'собрать':
            self.мост.отправить_сообщение(имя_бота, "🌾 Собираю ресурсы")
            self.боты[имя_бота]['еда'] += 10

    def показать_статистику(self):
        """Показывает текущую статистику"""
        print("\n📊 СТАТИСТИКА БИТВЫ:")
        for имя, данные in self.боты.items():
            print(f"  {имя}: HP:{данные['хп']} 🍗:{данные['еда']} ⚔️:{данные['убийств']}")


# Запуск
if __name__ == "__main__":
    глава = Глава()
    глава.загрузить_стратегии("стратегии")
    глава.создать_ботов()
    глава.запустить_битву()