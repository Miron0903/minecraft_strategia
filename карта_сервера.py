# карта_сервера.py - Преобразует реальные блоки в формат для стратегий
from enum import Enum


class ТипКлетки(Enum):
    ПУСТО = 0  # Воздух
    СТЕНА = 1  # Непроходимый блок
    ЕДА = 2  # Еда (пища, хлеб и т.д.)
    ВРАГ = 3  # Враждебный моб
    ИГРОК = 4  # Другой игрок
    РУДА = 5  # Ценные ресурсы
    ВОДА = 6  # Вода
    ЛАВА = 7  # Лава (опасно)
    ТРАВА = 8  # Обычный ландшафт


class КартаСервера:
    """
    Адаптирует реальные блоки Minecraft в формат для стратегий
    """

    # Словарь соответствия блоков Minecraft нашим типам
    БЛОКИ = {
        # Воздух
        'air': ТипКлетки.ПУСТО,
        'cave_air': ТипКлетки.ПУСТО,
        'void_air': ТипКлетки.ПУСТО,

        # Стены (непроходимые)
        'stone': ТипКлетки.СТЕНА,
        'cobblestone': ТипКлетки.СТЕНА,
        'bedrock': ТипКлетки.СТЕНА,
        'obsidian': ТипКлетки.СТЕНА,
        'dirt': ТипКлетки.СТЕНА,
        'grass_block': ТипКлетки.СТЕНА,

        # Еда
        'wheat': ТипКлетки.ЕДА,
        'carrots': ТипКлетки.ЕДА,
        'potatoes': ТипКлетки.ЕДА,
        'beetroots': ТипКлетки.ЕДА,
        'apple': ТипКлетки.ЕДА,
        'bread': ТипКлетки.ЕДА,

        # Враги (мобы)
        'zombie': ТипКлетки.ВРАГ,
        'skeleton': ТипКлетки.ВРАГ,
        'spider': ТипКлетки.ВРАГ,
        'creeper': ТипКлетки.ВРАГ,

        # Руда
        'coal_ore': ТипКлетки.РУДА,
        'iron_ore': ТипКлетки.РУДА,
        'gold_ore': ТипКлетки.РУДА,
        'diamond_ore': ТипКлетки.РУДА,
        'emerald_ore': ТипКлетки.РУДА,

        # Жидкости
        'water': ТипКлетки.ВОДА,
        'lava': ТипКлетки.ЛАВА,
    }

    def __init__(self, бот_мост):
        self.бот = бот_мост
        self.кэш = {}

    def получить_карту_для_стратегии(self, имя_бота, радиус=10):
        """
        Получает реальную карту с сервера и преобразует для стратегии
        """
        # Получаем позицию бота
        позиция = self.бот.получить_позицию(имя_бота)
        центр_x = позиция['x']
        центр_y = позиция['y']
        центр_z = позиция['z']

        # Сканируем область
        реальная_карта = self.бот.сканировать_область(
            имя_бота, центр_x, центр_y, центр_z, радиус
        )

        # Преобразуем в формат для стратегий
        карта_для_стратегии = {
            'игрок': {
                'x': центр_x,
                'y': центр_y,
                'z': центр_z,
                'хп': 100,  # Нужно получать реальное HP
                'еда': 100,  # Нужно получать реальную еду
            },
            'клетки': [],
            'враги': [],
            'ресурсы': []
        }

        for (x, y, z), блок in реальная_карта.items():
            тип_блока = блок.get('тип', 'air')
            наш_тип = self.БЛОКИ.get(тип_блока, ТипКлетки.СТЕНА)

            клетка = {
                'x': x,
                'y': y,
                'z': z,
                'тип': наш_тип,
                'прочность': блок.get('прочность', 100),
                'свет': блок.get('свет', 15)
            }

            карта_для_стратегии['клетки'].append(клетка)

            # Отдельно собираем врагов и ресурсы
            if наш_тип == ТипКлетки.ВРАГ:
                карта_для_стратегии['враги'].append(клетка)
            elif наш_тип == ТипКлетки.РУДА:
                карта_для_стратегии['ресурсы'].append(клетка)

        return карта_для_стратегии